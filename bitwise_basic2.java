public class bitwise_basic2 {
    public static void main(String[] args) {
        
        int x=9,y=10;
        System.out.println(x|y);
        System.out.println(x&y);
        System.out.println(x^y);
        System.out.println(~y);
        System.out.println(x>>2);
        System.out.println(x<<2);



    }
    
}
